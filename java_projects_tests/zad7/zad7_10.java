public class zad7_10 {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3};
        prArr(arr);
        System.out.println();
    }
    public static void prArr(int[] arr) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}
