public class HelloWorld {
    public static void main(String[] args) {
        // Просто выводим текст на экран
        System.out.println("Привет, мир!");
        
        // Создаем переменную для текста
        String имя = "Никита";
        System.out.println("Меня зовут " + имя);
        
        // Создаем переменную для числа
        int возраст = 20;
        System.out.println("Мне " + возраст + " лет");
        
        // Простые вычисления
        int a = 5;
        int b = 3;
        int сумма = a + b;
        System.out.println(a + " + " + b + " = " + сумма);
    }
}


